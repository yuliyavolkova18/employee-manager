    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        try {
            Class.forName("com.mysql.jdbc.Driver");
            java.sql.Connection con = DriverManager.getConnection("jdbc:mysql://localhost:8889/employee_manager", "root", "root");
            String sql_employee = "Delete from employee_table where id=?";
            String sql_absence = "Delete from absent_table where id=?";
            
            PreparedStatement pst_employee = con.prepareStatement(sql_employee);
            PreparedStatement pst_absence = con.prepareStatement(sql_absence);

            Integer id = Integer.parseInt(employee_id.getText());
            pst_employee.setInt(1, id);
            pst_absence.setInt(1, id);

            pst_employee.executeUpdate();
            pst_absence.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Employee deleted successfully!");
            con.close();

            new main_menu().setVisible(false);
            new main_menu().setVisible(true);
            dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_deleteActionPerformed

